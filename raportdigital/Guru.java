    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String nnip = nipp.getText();
        String namaguruu = namaguru.getText();
        String jk = (String) jeniskelamin.getSelectedItem();
        String mata = mapel.getText();
        
        try{
            String sql = "Update guru set nama_guru= ?, jenis_kelamin_guru= ?, mata_pelajaran= ? where nip= ?";
            PreparedStatement ps = Connection.prepareStatement(sql);
            ps.setString(1, namaguruu);
            ps.setString(2, jk);
            ps.setString(3, mata);
            ps.setString(4, nnip);
            ps.executeUpdate();
            
            this.id_auto();
            tampilkanguru(tabelguru);
            
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diupdate, karena:" + e.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    private void tampilkanguru(JTable guruTable){
        try{
            String sql = "Select * from guru order by nip";
            PreparedStatement pst = Connection.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("NIP");
            model.addColumn("Nama Guru");
            model.addColumn("Jenis Kelamin");
            model.addColumn("Mata Pelajaran");
            while(rs.next()){
                Object[] rowData = new Object[4];
                rowData[0] = rs.getString("nip");
                rowData[1] = rs.getString("nama_guru");
                rowData[2] = rs.getString("jenis_kelamin_guru");
                rowData[3] = rs.getString("mata_pelajaran");
                
            model.addRow(rowData);
            }
            tabelguru.setModel(model);
        }catch(SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }
