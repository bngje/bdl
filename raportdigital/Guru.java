    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:    
        try {
            String nnip = !nipp.getText().trim().isEmpty() ? nipp.getText().trim() : tabelguru.getValueAt(tabelguru.getSelectedRow(), 0).toString();
            String sql = "delete from guru where nip = ?";
            PreparedStatement ps = Connection.prepareStatement(sql);
            ps.setString(1, nnip);
            ps.execute();

            tampilkanguru(tabelguru);
            nipp.setText("");
            namaguru.setText("");
            mapel.setText("");
            id_auto();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
        } catch (SQLException ex) {
            Logger.getLogger(Guru.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

